package asciicrawler;

/**
 * \brief This enum defines all walking directions of the mobs.
 *
 * @author Leon Hansen, Felix Schmidt
 * @version 1.0
 */
public enum Direction {
	STAY		/**< state mob doesn't move */,
	UP			/**< state mob move up */,
	DOWN		/**< state mob move down */,
	LEFT		/**< state mob move left */,
	RIGHT		/**< state mob move right */
};
